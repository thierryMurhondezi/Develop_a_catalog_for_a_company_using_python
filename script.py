def print_circum(radius):
    circumference = 2 * 3.14159 * radius
    print("The circumference of the circle with radius", radius, "is", circumference)

print_circum(2)
# Output: The circumference of the circle with radius 2 is 12.56636

print_circum(5)
# Output: The circumference of the circle with radius 5 is 31.4159

print_circum(10)
# Output: The circumference of the circle with radius 10 is 62.8318